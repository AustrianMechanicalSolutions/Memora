@Injectable({ providedIn: 'root' })
export class I18nService {
  private readonly storageKey = 'memora.language';
  private readonly languageSubject = new BehaviorSubject<AppLanguage>(this.getInitialLanguage());
  readonly language$ = this.languageSubject.asObservable();

  get currentLanguage(): AppLanguage {
    return this.languageSubject.value;
  }

  setLanguage(language: AppLanguage) {
    this.languageSubject.next(language);
    localStorage.setItem(this.storageKey, language);
    document.documentElement.lang = language;
  }

  translate(key: string, params?: TranslationParams): string {
    const dict = TRANSLATIONS[this.currentLanguage];
    const fallback = TRANSLATIONS.en[key] ?? key;
    const template = dict[key] ?? fallback;

    if (!params) return template;

    return template.replace(/\{(\w+)\}/g, (_, param: string) => `${params[param] ?? ''}`);
  }

  private getInitialLanguage(): AppLanguage {
    const stored = localStorage.getItem(this.storageKey);
    if (stored === 'de' || stored === 'en') {
      document.documentElement.lang = stored;
      return stored;
    }

    const browser = navigator.language.toLowerCase().startsWith('de') ? 'de' : 'en';
    document.documentElement.lang = browser;
    return browser;
  }
}
